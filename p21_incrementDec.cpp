

#include<iostream>
#include<conio.h>

using namespace std;
//++ increment operator
//-- decrement operator
int main()
{

int a=10;
cout<<'\n'<<a;
++a;  //a=a+1;  Prefix form
cout<<endl<<a;	//endl stands for endline
a++;		//Postfix form
cout<<endl<<a;
a--;
cout<<"\n"<<a;
--a;
cout<<'\n'<<a;

return 0;
}


