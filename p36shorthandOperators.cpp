

#include<iostream>
using namespace std;

/*Shorthand operators or assignment operators*/
int main()
{
int a=15;
a+=10;   //a=a+10
cout<<"\na+=10 ="<<a;
a-=3;   //a=a-3
cout<<"\na-=3 ="<<a;
a*=5;   //a=a*5
cout<<"\na*=5 ="<<a;
a/=10;   //a=a/10
cout<<"\na/=10 ="<<a;
a %=3;   //a=a%3
cout<<"\na%=3 ="<<a;
return 0;
}


