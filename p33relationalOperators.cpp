

#include<iostream>
using namespace std;

//Relational operators
int main()
{
int a,b;
cout<<"\nEnter value of a=";
cin>>a;
cout<<"\nEnter value of b=";
cin>>b;
cout<<"\na<b="<<(a<b);
cout<<"\na<=b="<<(a<=b);
cout<<"\na>b="<<(a>b);
cout<<"\na>=b="<<(a>=b);
cout<<"\na==b="<<(a==b);
cout<<"\na!=b="<<(a!=b);
cout<<"\n!a="<<(!a);  //(a==0)
cout<<"\na="<<(a=b);
return 0;
}


